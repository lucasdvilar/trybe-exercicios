const people = [
  { name: 'Mateus', age: 18 },
  { name: 'José', age: 16 },
  { name: 'Ana', age: 23 },
  { name: 'Cláudia', age: 20 },
  { name: 'Bruna', age: 19 },
];

const verifyAges = (arr, minimumAge) => {
  const isOfAge = arr.every((person) => person.age >= minimumAge);

  return isOfAge;
}

console.log(verifyAges(people, 18));